    private void idnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_idnActionPerformed
        String selectedId = (String) idn.getSelectedItem();
        if (selectedId == null) {
            return;
        }

        EntityManagerFactory emf = Persistence.createEntityManagerFactory("TugasLimaPU");
        EntityManager em = emf.createEntityManager();

        try {
            Nasabah_1 nasabah = em.find(Nasabah_1.class, selectedId);
            if (nasabah == null) {
                JOptionPane.showMessageDialog(this,
                        "ID Nasabah tidak ditemukan di database!",
                        "Peringatan", JOptionPane.WARNING_MESSAGE);
            }

        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            em.close();
            emf.close();
        }
    }//GEN-LAST:event_idnActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (kode.getText().trim().isEmpty() || idn.getSelectedItem() == null
                || tanggal.getDate() == null || jenis.getSelectedItem() == null
                || jumlah.getText().trim().isEmpty()) {

            JOptionPane.showMessageDialog(this, "Semua field harus diisi!");
            return;
        }

        String idTransaksiBaru = kode.getText().trim();
        String idNasabah = (String) idn.getSelectedItem();
        String jenisTransaksi = (String) jenis.getSelectedItem();
        java.util.Date tanggalUtil = tanggal.getDate();
        BigDecimal jumlahBig;

        try {
            jumlahBig = new BigDecimal(jumlah.getText().trim());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Jumlah harus berupa angka!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        EntityManagerFactory emf = null;
        EntityManager em = null;

        try {
            emf = Persistence.createEntityManagerFactory("TugasLimaPU");
            em = emf.createEntityManager();
            em.getTransaction().begin();

            Transaksi transaksi = em.find(Transaksi.class, oldIdTransaksi);

            Nasabah_1 nasabah = em.find(Nasabah_1.class, idNasabah);
            if (nasabah == null) {
                JOptionPane.showMessageDialog(this, "Nasabah tidak ditemukan!", "Error", JOptionPane.ERROR_MESSAGE);
                em.getTransaction().rollback();
                return;
            }

            if (transaksi == null) {

                transaksi = new Transaksi();
                transaksi.setKodeTransaksi(idTransaksiBaru);
                transaksi.setTanggalTransaksi(new java.sql.Date(tanggalUtil.getTime()));
                transaksi.setJenisTransaksi(jenisTransaksi);
                transaksi.setJumlah(jumlahBig);
                transaksi.setIdnasabah(nasabah);
                em.persist(transaksi);
            } else {

                transaksi.setTanggalTransaksi(new java.sql.Date(tanggalUtil.getTime()));
                transaksi.setJenisTransaksi(jenisTransaksi);
                transaksi.setJumlah(jumlahBig);
                transaksi.setIdnasabah(nasabah);
                em.merge(transaksi);
            }

            BigDecimal saldo = nasabah.getSaldo();
            if ("Setor".equalsIgnoreCase(jenisTransaksi)) {
                saldo = saldo.add(jumlahBig);
            } else if ("Tarik".equalsIgnoreCase(jenisTransaksi)) {
                saldo = saldo.subtract(jumlahBig);
            }
            nasabah.setSaldo(saldo);

            em.merge(nasabah);
            em.getTransaction().commit();

            JOptionPane.showMessageDialog(this, "Transaksi berhasil disimpan!");
            if (parentFrame != null) {
                parentFrame.tampilkanData();
                parentFrame.tampilkanDataTransaksi();
            }
            this.dispose();

        } catch (Exception e) {
            if (em != null && em.getTransaction().isActive()) {
                em.getTransaction().rollback();
            }
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        } finally {
            if (em != null) {
                em.close();
            }
            if (emf != null) {
                emf.close();
            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed

