    private void loadNasabahToComboBox() {
        EntityManagerFactory emf = Persistence.createEntityManagerFactory("TugasLimaPU");
        EntityManager em = emf.createEntityManager();

        try {
            List<Nasabah_1> listNasabah = em.createQuery("SELECT n FROM Nasabah_1 n", Nasabah_1.class).getResultList();
            idn.removeAllItems();

            for (Nasabah_1 n : listNasabah) {
                idn.addItem(n.getIdnasabah());
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat data nasabah: " + e.getMessage());
            e.printStackTrace();
        } finally {
            em.close();
            emf.close();
        }
    }

