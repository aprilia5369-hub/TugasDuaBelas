    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (txtID.getText().equals("") || txtNama.getText().equals("")
                || txtAlamat.getText().equals("") || txtSaldo.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Semua field harus diisi!");
            return;
        }

        String id = txtID.getText();
        String nama = txtNama.getText();
        String alamat = txtAlamat.getText();
        double saldo;
        

        try {
            saldo = Double.parseDouble(txtSaldo.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Saldo harus berupa angka!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        EntityManagerFactory emf = null;
        EntityManager em = null;

        try {
            emf = Persistence.createEntityManagerFactory("TugasLimaPU");
            em = emf.createEntityManager();

            em.getTransaction().begin();

            Nasabah_1 nasabah = em.find(Nasabah_1.class, oldIdNasabah);
            if (nasabah == null) {
                JOptionPane.showMessageDialog(this, "Data nasabah tidak ditemukan!", "Error", JOptionPane.ERROR_MESSAGE);
                em.getTransaction().rollback();
                return;
            }

            
            if (!id.equals(oldIdNasabah)) {
                Nasabah_1 check = em.find(Nasabah_1.class, id);
                if (check != null) {
                    JOptionPane.showMessageDialog(this, "ID Nasabah baru sudah digunakan!", "Gagal", JOptionPane.ERROR_MESSAGE);
                    em.getTransaction().rollback();
                    return;
                }

               
                em.remove(nasabah);
                em.flush(); 

                Nasabah_1 baru = new Nasabah_1();
                baru.setIdnasabah(id);
                baru.setNama(nama);
                baru.setAlamat(alamat);
                baru.setSaldo(BigDecimal.valueOf(saldo));
                em.persist(baru);
            } else {
               
                nasabah.setNama(nama);
                nasabah.setAlamat(alamat);
                nasabah.setSaldo(BigDecimal.valueOf(saldo));
                em.merge(nasabah);
            }

            em.getTransaction().commit();

            JOptionPane.showMessageDialog(this, "Data berhasil diupdate!");

            
            if (getParent() instanceof Nasabah) {
                ((Nasabah) getParent()).tampilkanData();
            }

            dispose();

        } catch (Exception e) {
            if (em != null && em.getTransaction().isActive()) {
                em.getTransaction().rollback();
            }
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        } finally {
            if (em != null) em.close();
            if (emf != null) emf.close();
        }

    }//GEN-LAST:event_jButton1ActionPerformed

