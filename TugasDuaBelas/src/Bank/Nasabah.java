    public void tampilkanData() {
        EntityManager em = Persistence.createEntityManagerFactory("TugasLimaPU").createEntityManager();

        TypedQuery<Nasabah_1> q = em.createNamedQuery("Nasabah_1.findAll", Nasabah_1.class);
        List<Nasabah_1> hasil = q.getResultList();

        DefaultTableModel tbn = new DefaultTableModel(
                new String[]{"ID NASABAH", "NAMA", "ALAMAT", "SALDO"}, 0
        );

        for (Nasabah_1 data : hasil) {
            Object[] baris = new Object[4];
            baris[0] = data.getIdnasabah();
            baris[1] = data.getNama();
            baris[2] = data.getAlamat();
            baris[3] = data.getSaldo();
            tbn.addRow(baris);
        }

        Tabel.setModel(tbn);
        em.close();
    }

    public void tampilkanDataTransaksi() {
        EntityManager em = Persistence.createEntityManagerFactory("TugasLimaPU").createEntityManager();

        try {
            TypedQuery<Transaksi> q = em.createNamedQuery("Transaksi.findAll", Transaksi.class);
            List<Transaksi> hasil = q.getResultList();

            DefaultTableModel tbn = new DefaultTableModel(
                    new String[]{"Kode Transaksi", "Id Nasabah", "Tanggal Transaksi", "Jenis Transaksi", "Jumlah"}, 0
            );

            java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("yyyy-MM-dd");

            for (Transaksi data : hasil) {
                Object[] baris = new Object[5];
                baris[0] = data.getKodeTransaksi();
                baris[1] = data.getIdnasabah().getIdnasabah();

                if (data.getTanggalTransaksi() != null) {
                    baris[2] = sdf.format(data.getTanggalTransaksi());
                } else {
                    baris[2] = "";
                }

                baris[3] = data.getJenisTransaksi();
                baris[4] = data.getJumlah();
                tbn.addRow(baris);
            }

            tabelTransaksi.setModel(tbn);
        } catch (Exception e) {
            e.printStackTrace();
            javax.swing.JOptionPane.showMessageDialog(this,
                    "Gagal menampilkan data transaksi: " + e.getMessage(),
                    "Error",
                    javax.swing.JOptionPane.ERROR_MESSAGE);
        } finally {
            em.close();
        }
    }

    

