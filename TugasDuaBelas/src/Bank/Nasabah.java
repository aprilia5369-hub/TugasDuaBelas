    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        int index = tabPane.getSelectedIndex();
        String title = tabPane.getTitleAt(index);

        JFileChooser jfc = new JFileChooser(FileSystemView.getFileSystemView().getHomeDirectory());
        int returnValue = jfc.showOpenDialog(null);
        if (returnValue != JFileChooser.APPROVE_OPTION) {
            return;
        }

        File filePilihan = jfc.getSelectedFile();
        System.out.println("File dipilih: " + filePilihan.getAbsolutePath());

        BufferedReader br = null;
        EntityManagerFactory emf = null;
        EntityManager em = null;

        try {
            br = new BufferedReader(new FileReader(filePilihan));
            emf = Persistence.createEntityManagerFactory("TugasLimaPU");
            em = emf.createEntityManager();

            em.getTransaction().begin();

           
            br.mark(4096);
            String header = br.readLine();
            if (header == null) {
                JOptionPane.showMessageDialog(null, "File CSV kosong!");
                return;
            }

            String pemisah;
            if (header.contains(";")) {
                pemisah = ";";
            } else if (header.contains(",")) {
                pemisah = ",";
            } else if (header.contains("\t")) {
                pemisah = "\t";
            } else {
                pemisah = ";"; 
            }
            br.reset(); 

            
            if (title.equalsIgnoreCase("Nasabah")) {
                String baris;
                while ((baris = br.readLine()) != null) {
                    if (baris.trim().isEmpty()) {
                        continue;
                    }
                    String[] data = baris.split(pemisah);
                    if (data.length < 4) {
                        continue; 
                    }
                    String id = data[0].trim();
                    String nama = data[1].trim();
                    String alamat = data[2].trim();
                    String saldoStr = data[3].trim();

                    if (!id.isEmpty() && !nama.isEmpty() && !alamat.isEmpty() && !saldoStr.isEmpty()) {
                        double saldo = Double.parseDouble(saldoStr.replace(",", "."));

                        
                        if (em.find(Nasabah_1.class, id) != null) {
                            System.out.println(" Nasabah " + id + " sudah ada, dilewati.");
                            continue;
                        }

                        Nasabah_1 n = new Nasabah_1();
                        n.setIdnasabah(id);
                        n.setNama(nama);
                        n.setAlamat(alamat);
                        n.setSaldo(BigDecimal.valueOf(saldo));

                        em.persist(n);
                    }
                }

                em.getTransaction().commit();
                JOptionPane.showMessageDialog(null, "Upload selesai!");
                tampilkanData(); 
            } 
            else if (title.equalsIgnoreCase("Transaksi")) {
                int berhasil = 0;
                int gagal = 0;

                DefaultTableModel model = (DefaultTableModel) tabelTransaksi.getModel();
                

                String baris;
                while ((baris = br.readLine()) != null) {
                    if (baris.trim().isEmpty()) {
                        continue;
                    }

                    String[] data = baris.split(pemisah);
                    if (data.length < 5) {
                        System.out.println("Kolom kurang: " + baris);
                        gagal++;
                        continue;
                    }

                    try {
                        String idTransaksi = data[0].trim();
                        String idNasabah = data[1].trim();
                        String tanggalStr = data[2].trim();
                        String jenis = data[3].trim();
                        String jumlahStr = data[4].trim();

                        jumlahStr = jumlahStr.replace(".", "").replace(",", ".");
                        BigDecimal jumlah = new BigDecimal(jumlahStr);

                        Date tanggal;
                        if (tanggalStr.contains("-")) {
                            tanggal = new SimpleDateFormat("yyyy-MM-dd").parse(tanggalStr);
                        } else if (tanggalStr.contains("/")) {
                            tanggal = new SimpleDateFormat("dd/MM/yyyy").parse(tanggalStr);
                        } else {
                            throw new IllegalArgumentException("Format tanggal tidak dikenali: " + tanggalStr);
                        }

                        Nasabah_1 nasabah = em.find(Nasabah_1.class, idNasabah);
                        if (nasabah == null) {
                            System.out.println(" Nasabah " + idNasabah + " tidak ditemukan!");
                            gagal++;
                            continue;
                        }

                        if (em.find(Transaksi.class, idTransaksi) != null) {
                            System.out.println("ï¸ Transaksi " + idTransaksi + " sudah ada, dilewati.");
                            gagal++;
                            continue;
                        }

                        Transaksi t = new Transaksi();
                        t.setKodeTransaksi(idTransaksi);
                        t.setIdnasabah(nasabah);
                        t.setTanggalTransaksi(tanggal);
                        t.setJenisTransaksi(jenis);
                        t.setJumlah(jumlah);
                        em.persist(t);

                        if ("Setor".equalsIgnoreCase(jenis)) {
                            nasabah.setSaldo(nasabah.getSaldo().add(jumlah));
                        } else if ("Tarik".equalsIgnoreCase(jenis)) {
                            nasabah.setSaldo(nasabah.getSaldo().subtract(jumlah));
                        }
                        em.merge(nasabah);

                        berhasil++;

                    } catch (Exception eRow) {
                        gagal++;
                        System.out.println(" Gagal memproses baris: " + baris);
                        eRow.printStackTrace();
                    }
                }

                em.getTransaction().commit();
                JOptionPane.showMessageDialog(null, "Upload selesai!");
                tampilkanData();
                tampilkanDataTransaksi();
            }

        } catch (Exception ex) {
            ex.printStackTrace();
            if (em != null && em.getTransaction().isActive()) {
                em.getTransaction().rollback();
            }
            JOptionPane.showMessageDialog(null, "Terjadi kesalahan: " + ex.getMessage());
        } finally {
            try {
                if (br != null) {
                    br.close();
                }
            } catch (IOException e) {
            }
            if (em != null) {
                em.close();
            }
            if (emf != null) {
                emf.close();
            }
        }

    }//GEN-LAST:event_jButton5ActionPerformed

